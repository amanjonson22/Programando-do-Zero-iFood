// operadores de igual

// = é atribuição
let numero = 1
console.log(numero)

numero = 5
console.log(numero)

// == é para comparar o valor
console.log("número é igual a 5? " + numero == 5 + "  número é igual a 1? " + numero == 1)

// === é para comparar o valor e o formato
console.log(numero === "5")
console.log(numero == "5")